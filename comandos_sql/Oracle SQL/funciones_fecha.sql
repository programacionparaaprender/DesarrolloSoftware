-- MONTHS_BETWEEN si el primero menor que el segundo los digitos salen negativos.
select 
SYSDATE, 
ADD_MONTHS(SYSDATE, -1) anterior,
ADD_MONTHS(SYSDATE, 12) siguiente,
MONTHS_BETWEEN('31/03/08','30/09/08') f1,
MONTHS_BETWEEN('31/03/08','30/09/08') f2,
MONTHS_BETWEEN('31/03/08','30/09/08') f3,
MONTHS_BETWEEN('31/03/08','30/03/07') f4,
ROUND(MONTHS_BETWEEN('31/03/08','30/03/07'), 1) f5,
LAST_DAY(SYSDATE) fin_de_mes,
LAST_DAY(SYSDATE) + 1 siguiente,
ROUND(SYSDATE,'HH24') ROUND_HOUR,
ROUND(SYSDATE,'MM') ROUND_MONTH,
ROUND(SYSDATE) ROUND_DATE,
LAST_DAY(CURRENT_TIMESTAMP) + 1 siguiente2,
ROUND(CURRENT_TIMESTAMP,'HH24') ROUND_HOUR2,
ROUND(CURRENT_TIMESTAMP,'MM') ROUND_MONTH2,
ROUND(CURRENT_TIMESTAMP) ROUND_DATE2
FROM 
DUAL